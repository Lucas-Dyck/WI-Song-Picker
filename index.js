function totalSongs(songLibrary){
    songs = 0;
    for (let i=0; i < songLibrary.length; i++){
        songs += songLibrary[i].songsArray.length;
    }
    return songs;
}

document.getElementById("pickfrom").textContent = (`Pick from ${totalSongs(songLibrary)} songs and ${songLibrary.length} albums!`);

var button = document.getElementById("button");

function pickSong(songLibrary){
    var album_dict = choice(songLibrary);
    var album = album_dict.album;
    var song = choice(album_dict.songsArray);
    if (song === album) {
        album += " - Single";
    }
    return [song, album];
}

button.addEventListener("click", function(){
    handleClick(songLibrary);
});

function choice(array) {
    return array[Math.floor(Math.random() * array.length)];
}

function handleClick(songLibrary){
    var songArray = pickSong(songLibrary);
    var song = songArray[0];
    var album = songArray[1];
    document.getElementById("song").textContent = song;
    document.getElementById("album").textContent = album;
    button.classList.add("clicked");
    setTimeout(function(){button.classList.remove("clicked")}, 200);
    processedURL = processURL(song);
    document.querySelector("a").setAttribute("href", processedURL);
} 

function processURL(song){
    song = song.toLowerCase().replaceAll(" ", "-").replaceAll("(", "-").replaceAll(")", "").replaceAll(",", "-").replaceAll(":", "-").replaceAll(/'/g, "-").replaceAll("--", "-");
    if (song === "all-glory-be-to-christ") {
        song+="-8aeb1bd6-6af8-4a8c-90e7-b8dfe768eec3";
    }
    return ("https://app.theworshipinitiative.com/songs/" + song + "/instrument/chart");
}
